#include "../cub3d.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned	char	*str1;
	unsigned	char	*str2;
	size_t				i;

	if (ft_strlen(s1) != ft_strlen(s2))
		return (2);
	str1 = (unsigned char*)s1;
	str2 = (unsigned char*)s2;
	i = 0;
	while (i < n && (str1[i] != '\0' || str2[i] != '\0'))
	{
		if (str1[i] > str2[i])
			return (1);
		else if (str1[i] < str2[i])
			return (-1);
		i++;
	}
	return (0);
}
